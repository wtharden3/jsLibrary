// ASSIGNMENT OPERATOR
let r = 5;

// ADDITION ASSIGNMENT
r += 1; // same as r = r + 1;

// SUBTRACTION ASSIGNMENT
r -= 1; // same as r = r - 1;

//MULTIPLICATION ASSIGNMENT
r *= 1; // same as r = r * 1;

//DIVISION ASSIGNMENT 
r /= 1 // same as r = r / 1;

// MODULUS (remainder) ASSIGNMENT
r %= 1; // same as r = r % 1;

// EXPONENTIAL ASSIGNMENT
r **= 1; // same as r ** 1;