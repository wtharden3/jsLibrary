// COMPARISON OPERATORS

// EQUALITY
console.log('3' == 3); // true because the type is converted to same and then compared
console.log(3 == 3); // also true

// STRICT EQUALITY
console.log('3' === 3); //  false because type is not converted -- one is a string data type and one is a number data type
console.log(3 === 3); // true

// NOT EQUAL COMPARISON
console.log('3' != 3); // ! = "bang" but will be FALSE

// STRICT NOT EQUAL COMPARISON
console.log('3' !== 3); // True because types are not same even though it is the same "number"

// GREATER THAN
3 > 2;

// LESS THAN
2 < 3;

// GREATER THAN OR EQUAL TO
3 >= 2;

// LESS THAN OR EQUAL TO
2 <= 3;

// AND
2 && 3

// OR
2 || 3

console.log(3>2==1); // true because 3>2 is true so true>1 means 1 is equal to 1
console.log(3>2>0); // true